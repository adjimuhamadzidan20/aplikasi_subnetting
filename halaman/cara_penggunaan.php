	<h3>Cara Penggunaan</h3>	
</div>
<div class="cara-penggunaan mb-4 w-75" style="line-height: 28px; text-align: justify;">
	<ol>
		<li>
			Masukkan alamat IP atau IP Address beserta slash yang sudah ditentukan pada halaman IP Jaringan.
		</li>
		<li>
			Setelah memasukkan alamat IP atau IP Address lalu pindah ke halaman Host Jaringan, Tertera IP Address serta Slash
			yang sudah dimasukkan. Kemudian masukkan atau tambahkan Nama Divisi serta Jumlah host yang sudah ditentukan.
		</li>
		<li>
			Setelah sudah memasukkan semua data Nama Divisi serta Jumlah Host yang ditentukan, untuk melakukan proses perhitungan
			subnetting otomatis dapat mengklik button Generate untuk melakukan proses operasi perhitungan. 
		</li>
		<li>
			Ketika telah melakukan proses perhitungan maka akan menampilkan hasil generate perhitungan subnetting yang dilakukan otomatis
			, dalam hasil perhitungan terdapat beberapa hasil yang terdiri Semua Hasil, NetID, RangeID, dan terakhir BroadcastID.
		</li>
		<li>
			Pada data hasil perhitungan tersebut kita dapat mencetaknya dalam bentuk PDF atau Excel, kita dapat menjadikan hasil perhitungan
			subneting yang sudah di generate dalam bentuk file PDF dan Excel.
		</li>
	</ol>
</div>